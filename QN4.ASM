;PROGRAM TO ADD THE NUMBERS FROM 1 TO FIFTY AND DISLAY THE 16 BIT RESULT IN PORT A AND B
MVI C,32H;     INITIALISE THE COUNT AND FOR SUM
MVI A,00H;     FOR ADDING 
MVI D,00H;     FOR COUNTING THE CARRY


LOOP: ADD C

	JNC CARRY
		INR D		
	CARRY: DCR C
	
JNZ LOOP 
OUT 0AH
MOV A,D
OUT 0BH
HLT